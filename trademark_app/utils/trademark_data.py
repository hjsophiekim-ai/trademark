# 전체 상품 목록 (자동완성용)
GOODS_LIST = []
for class_no, info in CLASS_DATABASE.items():
    for goods in info["대표상품"]:
        GOODS_LIST.append({
            "goods": goods,
            "class_no": class_no,
            "류명": info["류명"],
            "유사군": info["유사군"],
            "label": f"{goods} ({class_no} - {info['류명']})",
        })


def get_goods_by_query(query: str) -> list[dict]:
    """검색어로 상품 필터링 (자동완성)"""
    if not query:
        return GOODS_LIST[:30]
    q = query.lower()
    return [g for g in GOODS_LIST if q in g["goods"].lower() or q in g["류명"].lower() or q in g["class_no"]]


def get_classes_for_goods(selected_goods: list[dict]) -> list[str]:
    """선택된 상품들의 류 목록 반환 (중복 제거)"""
    return list(dict.fromkeys(g["class_no"] for g in selected_goods))


def get_similar_codes_for_goods(selected_goods: list[dict]) -> list[str]:
    """선택된 상품들의 유사군 코드 목록 반환 (중복 제거)"""
    codes = []
    for g in selected_goods:
        for c in g["유사군"]:
            if c not in codes:
                codes.append(c)
    return codes
