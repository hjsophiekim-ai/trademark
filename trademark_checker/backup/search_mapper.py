def search_products(query: str) -> list[dict]:
    """
    상품명 입력 → 추천 류 목록 반환
    (직접 매칭 + 별칭 매칭 + 유사도 매칭)
    """
    if not query or len(query.strip()) < 1:
        return []

    q = query.strip().lower()

    # 별칭 확인
    resolved = ALIASES.get(q, q)

    results: list[dict] = []
    seen_classes: set[str] = set()

    def add_items(items: list[dict]):
        for item in items:
            if item["류"] not in seen_classes:
                results.append(item)
                seen_classes.add(item["류"])

    # 1) 정확 매칭
    if resolved in PRODUCT_SEARCH_MAP:
        add_items(PRODUCT_SEARCH_MAP[resolved])

    # 2) 포함 매칭 (쿼리가 키에 포함되거나 키가 쿼리에 포함)
    for key, items in PRODUCT_SEARCH_MAP.items():
        if key == resolved:
            continue
        if q in key or key in q or resolved in key or key in resolved:
            add_items(items)

    # 3) difflib 유사도 매칭
    all_keys = list(PRODUCT_SEARCH_MAP.keys())
    close = get_close_matches(q, all_keys, n=4, cutoff=0.55)
    close += get_close_matches(resolved, all_keys, n=3, cutoff=0.55)
    for key in close:
        add_items(PRODUCT_SEARCH_MAP[key])

    return results[:6]


