def _normalize(text: str) -> str:
    return text.strip().lower().replace(" ", "")


def _score(query: str, keyword: str) -> float:
    nq = _normalize(query)
    nk = _normalize(keyword)
    if nq == nk:
        return 1.0
    if nq in nk or nk in nq:
        return 0.92
    return SequenceMatcher(None, nq, nk).ratio()


def search_products(query: str, limit: int = 3) -> List[dict]:
    if not query.strip():
        return []

    normalized = _normalize(query)
    alias_target = ALIASES.get(normalized, normalized)
    seen = set()
    candidates = []

    for keyword, items in PRODUCT_SEARCH_MAP.items():
        score = max(_score(alias_target, keyword), _score(normalized, keyword))
        if score < 0.35:
            continue
        for item in items:
            key = (keyword, item["류"], item["설명"])
            if key in seen:
                continue
            seen.add(key)
            candidates.append({"키워드": keyword, "매칭점수": round(score, 3), **item})

    candidates.sort(key=lambda row: (-row["매칭점수"], row["류"], row["설명"]))
    return candidates[:limit]


def get_catalog() -> Dict[str, List[dict]]:
    goods = []
    services = []
    seen = set()

    for keyword, items in PRODUCT_SEARCH_MAP.items():
        for item in items:
            key = (item["류"], item["설명"])
            if key in seen:
                continue
            seen.add(key)
            payload = {"키워드": keyword, **item}
            class_no = int(item["류"].replace("류", ""))
            if class_no <= 34:
                goods.append(payload)
            else:
                services.append(payload)

    goods.sort(key=lambda row: int(row["류"].replace("류", "")))
    services.sort(key=lambda row: int(row["류"].replace("류", "")))
    return {"goods": goods, "services": services}
